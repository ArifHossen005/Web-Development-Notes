// Multiple Where Clause in SQL

1.find a perticular column data  in a table

SELECT * FROM table_name WHERE column_name = value1 AND column_name = value2;

2.multiple conditon check 

SELECT * FROM table_name WHERE column_name = value1 AND column_name = value2 AND column_name = value3;

3.multiple value find 

SELECT * FROM table_name WHERE column_name IN (value1,value2,value3);